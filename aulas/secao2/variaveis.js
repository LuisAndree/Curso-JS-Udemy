//     Variaveis {
//         As variáveis são declaradas usando a palavra-chave var. A sintaxe
//         é a seguinte:
        
        var nome = "André";
        
        // Neste exemplo, a variável nome é declarada e inicializada com o valor "André".
        
        // As variáveis declaradas com var têm escopo global ou de função. Isso significa 
        // que elas podem ser acessadas de qualquer lugar no código, dentro da função em 
        // que são criadas ou declaradas ou em todo o documento, se forem declaradas fora
        // de qualquer função.  
    // } 
    
    // Constantes {
    //     As constantes são declaradas usando a palavra-chave const. A sintaxe
    //     é a seginte:
        
        const PI = 3.14159;
    
    //     Neste exemplo, a constante PI é declarada e inicializada com o valor do número pi.
                                                                                                                                         
    //     As constantes declaradas com cosnt têm escopo de bloco. Isso significa que elas só 
    //     podem ser acessadas no bloco de código em que são declaradas.
    // }

    // Var, let e const {
    //     A principal diferença entre var e let é o escopo. Variáveis declaradas com var têm
    //     escopo global ou de função, enquanto variáveis declaradas com let têm escopo de bloco.

    //     A principal diferença entre var e const é a mutabilidade. Variáveis declaradas com var
